const switchStyle = {
  backgroundColor: '#614031',
  color: '#b2b2b2',
  alignSelf: 'center',
  margin: 0
}

module.exports = {

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  SwitchOn: (props) => {
    return (
      <Button rounded style={switchStyle} onPress={props.onSwitchOn}>
        <Text> ON </Text>
      </Button>
    )
  },
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  //
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  SwitchOff: (props) => {
    return (
      <Button rounded style={switchStyle} onPress={props.onSwitchOff}>
        <Text> OFF </Text>
      </Button>
    )
  }

}